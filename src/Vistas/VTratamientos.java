     private void cargarCombo(){
   
         jCombo.removeAllItems();
    listtrata=(ArrayList) tratdat.obtenerTratamientos();
    for(Tratamiento t:listtrata){
    jCombo.addItem(t);
    
    }
        jCombo.setSelectedIndex(-1);
       
    
    }
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
